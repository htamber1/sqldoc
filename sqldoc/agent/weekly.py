def _hot(n):
    return "#f87171" if n else "#e5e7eb"


def render_weekly_html(digest) -> str:
    cards = []
    for s in digest["databases"]:
        cards.append(_CARD.format(
            name=_esc(s["name"]),
            schema=len(s["schema_changes"]),
            pii=len(s["new_pii"]), pii_color=_hot(len(s["new_pii"])),
            pii_high=s["pii_high_now"], pii_trend=_trend_text(s["pii_score_delta"]),
            health_now=s["health_now"], health_trend=_trend_text(s["health_delta"]),
            jobs=len(s["job_failures"]), jobs_color=_hot(len(s["job_failures"])),
            infra=len(s["infra_alerts"]), infra_color=_hot(len(s["infra_alerts"]))))
    t = digest["totals"]
    return _HTML.format(
        generated=_esc(digest["generated_at"]), n_db=t["databases"],
        t_schema=t["schema_changes"], t_pii=t["new_pii"], t_jobs=t["job_failures"],
        t_infra=t["infra_alerts"], cards="\n".join(cards), version=__version__)


def _esc(s):
    return (str(s).replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;"))


# --- scheduling -------------------------------------------------------------

def _week_key(dt) -> str:
    """ISO year-week string, e.g. '2026-W28' — the idempotency key for a week."""
    iso = dt.isocalendar()
    return f"{iso[0]}-W{iso[1]:02d}"


def is_due(weekly_cfg, now, last_week_key) -> bool:
    """True when a digest should be sent now: it's the configured weekday at/after
    the configured hour, and we haven't already sent one this calendar week."""
    if not weekly_cfg or not weekly_cfg.enabled:
        return False
    if now.weekday() != weekly_cfg.weekday:
        return False
    if now.hour < weekly_cfg.hour:
        return False
    return _week_key(now) != last_week_key


def maybe_send_weekly_report(store, agent_config, now=None, log=lambda *_: None,
                             send_fn=None) -> bool:
    """If a weekly digest is due, build + email it and record the send. Returns
    True if a digest was sent. Safe to call frequently (idempotent per week)."""
    weekly = getattr(agent_config, "weekly_report", None)
    if not weekly or not weekly.enabled:
        return False
    smtp = getattr(agent_config.notify, "smtp", None)
    if not smtp:
        return False
    now = now or datetime.now()
    last = store.get_meta(_LAST_SENT_KEY)
    if not is_due(weekly, now, last):
        return False

    since_iso = _iso((datetime.now(timezone.utc) - timedelta(days=7)))
    db_names = [d.name for d in agent_config.databases]
    digest = build_weekly_digest(store, db_names, since_iso, now.strftime("%B %d, %Y"))
    html = render_weekly_html(digest)
    text = render_weekly_text(digest)
    subject = f"[sqldoc] Weekly digest - {now.strftime('%b %d, %Y')}"

    send = send_fn or notify_mod.send_html_email
    try:
        send(smtp, subject, html, text)
        store.set_meta(_LAST_SENT_KEY, _week_key(now))
        log(f"weekly digest emailed for {len(db_names)} database(s)")
        return True
    except Exception as e:
        log(f"weekly digest failed: {type(e).__name__}: {e}")
        return False
