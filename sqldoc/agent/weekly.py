"""Scheduled weekly email digest for the agent.

When `agent.weekly_report` is enabled, the daemon emails a formatted HTML digest
on the configured weekday/hour (default Monday 08:00 local) covering the past
week for every monitored database: schema changes, newly-exposed PII, health
trends, SQL Agent job failures, and infrastructure/security alerts.

The digest is built purely from what the agent already records in its store
(the event timeline + the metrics time-series), so no extra database access is
needed to produce it. Scheduling is idempotent — `maybe_send_weekly_report`
sends at most one digest per calendar week, tracked via a `kv` marker.
"""
from datetime import datetime, timedelta, timezone

from sqldoc import __version__
from sqldoc.agent import notify as notify_mod

# Event types that are security / infrastructure alerts (grouped in the digest).
INFRA_ALERT_TYPES = ("disk_low", "errorlog_critical", "linked_server_down",
                     "backup_stale", "replica_lag", "tempdb_version_store", "nl_alert")

_LAST_SENT_KEY = "weekly_report_last_sent"


def _iso(dt) -> str:
    return dt.replace(microsecond=0).isoformat()


def _delta(first, last, field):
    if first and last and first.get(field) is not None and last.get(field) is not None:
        return last[field] - first[field]
    return None


def _db_section(store, db_name, since_iso):
    events = store.events_since(since_iso, db_name)
    by_type = {}
    for e in events:
        by_type.setdefault(e["type"], []).append(e)
    metrics = store.metrics_since(since_iso, db_name)
    first = metrics[0] if metrics else None
    last = metrics[-1] if metrics else None
    infra = [e for e in events if e["type"] in INFRA_ALERT_TYPES]
    return {
        "name": db_name,
        "schema_changes": by_type.get("schema_change", []),
        "new_pii": by_type.get("new_pii", []),
        "job_failures": by_type.get("job_failure", []),
        "health_degradations": by_type.get("health_degradation", []),
        "infra_alerts": infra,
        "pii_score_delta": _delta(first, last, "pii_score"),
        "health_delta": _delta(first, last, "health_issues"),
        "pii_high_now": last["pii_high"] if last else None,
        "health_now": last["health_issues"] if last else None,
        "polls": len(metrics),
        "total_events": len(events),
    }


def build_weekly_digest(store, db_names, since_iso, generated_at_iso):
    sections = [_db_section(store, n, since_iso) for n in db_names]
    totals = {
        "schema_changes": sum(len(s["schema_changes"]) for s in sections),
        "new_pii": sum(len(s["new_pii"]) for s in sections),
        "job_failures": sum(len(s["job_failures"]) for s in sections),
        "infra_alerts": sum(len(s["infra_alerts"]) for s in sections),
        "databases": len(sections),
    }
    return {"since": since_iso, "generated_at": generated_at_iso,
            "databases": sections, "totals": totals}


# --- rendering --------------------------------------------------------------

def _trend_text(delta, lower_is_better=True):
    if delta is None:
        return "no data"
    if delta == 0:
        return "no change"
    worse = (delta > 0) if lower_is_better else (delta < 0)
    word = "worse" if worse else "improved"
    return f"{'+' if delta > 0 else ''}{delta} ({word})"


def render_weekly_text(digest) -> str:
    t = digest["totals"]
    lines = [
        "sqldoc weekly digest",
        f"Week ending {digest['generated_at']}",
        "",
        f"Across {t['databases']} database(s): {t['schema_changes']} schema change(s), "
        f"{t['new_pii']} new-PII event(s), {t['job_failures']} job failure(s), "
        f"{t['infra_alerts']} infrastructure/security alert(s).",
        "",
    ]
    for s in digest["databases"]:
        lines.append(f"== {s['name']} ==")
        lines.append(f"  Schema changes:      {len(s['schema_changes'])}")
        lines.append(f"  New PII findings:    {len(s['new_pii'])}  "
                     f"(HIGH now: {s['pii_high_now']}, score trend {_trend_text(s['pii_score_delta'])})")
        lines.append(f"  Health issues now:   {s['health_now']}  "
                     f"(trend {_trend_text(s['health_delta'])})")
        lines.append(f"  Job failures:        {len(s['job_failures'])}")
        lines.append(f"  Infra/security:      {len(s['infra_alerts'])}")
        lines.append("")
    lines.append(f"Generated by sqldoc v{__version__}.")
    return "\n".join(lines)


